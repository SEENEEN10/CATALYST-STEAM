const revealElements = document.querySelectorAll(".reveal");

const revealObserver = new IntersectionObserver(
  (entries, observer) => {
    entries.forEach((entry) => {
      if (!entry.isIntersecting) return;
      entry.target.classList.add("visible");
      observer.unobserve(entry.target);
    });
  },
  { threshold: 0.15 }
);

revealElements.forEach((el) => revealObserver.observe(el));

const languageButtons = document.querySelectorAll(".lang-btn");
const orderForm = document.getElementById("orderForm");
const orderStatus = document.getElementById("orderStatus");

function applyLanguage(lang) {
  const selectedLang = lang === "ar" ? "ar" : "en";
  const dictionary = translations[selectedLang];

  document.documentElement.lang = selectedLang;
  document.documentElement.dir = selectedLang === "ar" ? "rtl" : "ltr";
  document.body.classList.toggle("lang-ar", selectedLang === "ar");
  document.body.classList.toggle("lang-en", selectedLang === "en");

  document.title = dictionary.meta_title;

  const metaDescription = document.querySelector('meta[name="description"]');
  if (metaDescription) {
    metaDescription.setAttribute("content", dictionary.meta_description);
  }

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (!Object.prototype.hasOwnProperty.call(dictionary, key)) return;
    element.textContent = dictionary[key];
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.dataset.i18nPlaceholder;
    if (!Object.prototype.hasOwnProperty.call(dictionary, key)) return;
    element.setAttribute("placeholder", dictionary[key]);
  });

  languageButtons.forEach((button) => {
    button.classList.toggle("is-active", button.dataset.lang === selectedLang);
  });

  localStorage.setItem("catalyst_lang", selectedLang);
}

languageButtons.forEach((button) => {
  button.addEventListener("click", () => {
    applyLanguage(button.dataset.lang);
  });
});

const storedLanguage = localStorage.getItem("catalyst_lang");
const browserLanguage = (navigator.language || "").toLowerCase();
const initialLanguage = storedLanguage === "en" || storedLanguage === "ar"
  ? storedLanguage
  : browserLanguage.startsWith("ar")
    ? "ar"
    : "en";

applyLanguage(initialLanguage);

